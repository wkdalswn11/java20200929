package chap06.textbook.Exercise.p16;

public class Printer {
	

	// 메소드

	double println(double a) {
		System.out.println(a);
		return a;
	}

	String println(String b) {
		System.out.println(b);
		return b;
	}

	int println(int c) {
		System.out.println(c);
		return c;
	}

	boolean println(boolean d) {
		System.out.println(d);
		return d;
	}

}
