package chap02.type;

public class StringType {
	public static void main(String[] args) {
		// String: 문자열 저장
		String carName;
		carName = "Kia";
		
		System.out.println(carName);
		System.out.println("");
		System.out.println("여러 문자열...");
		System.out.println("큰따옴표\" 작은따옴표'");
		//  String은 0개 이상의 문자열을 가질수있음
	}

}
